    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
   
 //get user input from input fields
    String platform = txtplat.getText();
    String username = txtuser.getText();
    String password = new String(txtpass.getPassword()); //(user will have  to reinput again his pass if not strong)
        
//check if password strong        
SecurePasswordRule checker = new SecurePasswordRule();  //from securepasswordrule class (instintiation)
if (!checker.isPasswordStrong(password)) { //if not strong then prompt user (if returned false)
    prompt.setText("Password not strong enough!");
    txtpass.setText("");// clears the txtpass field
    return; //does not proceed (stops) 
}
///////////////////////////////////////////////////    
  // Save to DB (saving process) --> handler
    SQLITEDataBaseHandler dbHandler = new SQLITEDataBaseHandler(); //class instansiation
    boolean success = dbHandler.savePasswordEntry(username, password, platform); //call method (true/false)
    //uses the handler class specifically the savepassentry method

    if (success) { //if success prompt user
        prompt.setText("Password saved successfully.");
        //Clear input fields
        txtuser.setText("");
        txtpass.setText("");
        txtplat.setText("");
    } else { //not failed prompt user
        prompt.setText("Failed to save password.");
    }  


        
        
        
        
    }//GEN-LAST:event_saveActionPerformed
