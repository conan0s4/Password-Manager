    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
     
     //get user inputs (id)   
    String idStr = iddelete.getText();
    if (idStr.isEmpty()) { //check if id isempty --> if empty prompt user
        prompt.setText("Please enter an ID to delete.");
        return;
    }

    try { //convert id string to int
        int id = Integer.parseInt(idStr);
        SQLITEDataBaseHandler dbHandler = new SQLITEDataBaseHandler(); //dbhandler class instatiation   
        boolean success = dbHandler.deletePasswordEntry(id); //delete the (user,pass,platform) group  by its id 

        if (success) { //ifsuccess prompt user
            prompt.setText("Entry deleted successfully.");
            iddelete.setText("");
        } else { //ifnot then prompt user
            prompt.setText("Entry not found or failed to delete.");
        }
    } catch (NumberFormatException e) { //handles non-integer inputs (invalid id formats)
        prompt.setText("Invalid ID format.");
    }







    }//GEN-LAST:event_deleteActionPerformed

    
    
    
    
