    private void viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewActionPerformed
   
 
        
    SQLITEDataBaseHandler dbHandler = new SQLITEDataBaseHandler(); //class instatiation
    ResultSet rs = dbHandler.viewPasswordEntries(); 
    //calls a method viewPasswordEntries --> returns  ResultSet containing rows from your password table.
    
    
    
    //prepares table for new data------>
    try { //gets the structure of the outputtable --> cast to defualt table model to add rows
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tableoutput.getModel();//---> make modl editable
//                                   identifier --> assign-->convert to editable-->identifier of the table in gui-->get tables data        
        model.setRowCount(0); // Clear previous rows or existing (old) data before adding a new row
        //(prevents duplication  every time view button is clicked)

        
        
        while (rs.next()) { //loop through each row in the resualtset
            // Get data from each column of the current row
            int id = rs.getInt("id"); //get (id,user,psw,plat)
            String user = rs.getString("user"); 
            String pass = rs.getString("password");
            String plat = rs.getString("platform");
 
            model.addRow(new Object[]{id, user, pass, plat}); //Adds a new row to the (here) table using the values retrieved from the database.
        } /*Extracts ID, username, password, and platform from each DB row/adds each entry as a new row in the table.*/
        prompt.setText("Entries loaded."); //prompts user if entry was loadedsuccessfully 
    } catch (SQLException e) {
        prompt.setText("Error loading entries."); //error handling
        e.printStackTrace();
    }     
        
        
        
        
    }//GEN-LAST:event_viewActionPerformed

    
    
    
    
    
    
    
    
    
    
    
